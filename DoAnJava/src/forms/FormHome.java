    private void btnKiemtraidActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnKiemtraidActionPerformed
        int id = Integer.parseInt(txtChitietID.getText());
        ThanhVienBL.kiemtraIDtv(id);
        if(ThanhVien.lstThanhVien.size()<=0){
            JOptionPane.showMessageDialog(rootPane,"Không tìm thấy ID này !!!");
            txtChitietID.setText("");
            txtChitietTt.setText("");
            txtChitietbienlai.setText("");
        }else{
            JOptionPane.showMessageDialog(rootPane, "Đã tìm thấy thông tin thành viên này");
            for(ThanhVien tv:ThanhVien.lstThanhVien){
                txtChitietID.setText(String.valueOf(tv.getiId()));
                txtChitietTt.setText(tv.getStrChitiettt());
                txtChitietbienlai.setText(tv.getStrChitietbl());
                btnGui.setEnabled(true);
            }
            ThanhVien.lstThanhVien.clear();
        
        }       
    }//GEN-LAST:event_btnKiemtraidActionPerformed

