    void initThanhvien(){
        List<ThanhVien> listSV;
        listSV = ThanhVienBL.dsThanhVien();
        System.out.println(listSV.size());
        if(listSV.size()>0){
            model = (DefaultTableModel)jtb1.getModel();
            model.setRowCount(0);
            for(ThanhVien s:listSV)
            {   
                model.addRow(new Object[]{s.getStrHoten(),s.getiSdt(),s.getStrDiachi(),s.getStrDangkytren(),s.getStrDangkyboi()});
            }
        }
    }
